/*

Molde para função for (REPETIÇÃO)

	for(variável;condição;salto)
	
EXEMPLO 1:

	for(i=1;i<=10;i++){
		printf("i: %d\n", i)
	}
	
	i=1: ponto inicial da variável (iniciação)
	i<=10: condição (repete enquanto i for menor ou igual a 10)
	i++: como a variável vai mudar em cada ciclo (incremento de 1 em 1)
	
	a repetição vai continuar até a variável não corresponder mais com a condição.
	nesse caso: quando a variável i for maior que 10 a estrutura de repetição finaliza.
	
EXEMPLO 2:
	
	for(i=1;i<=10;i+=2){
	printf("i: %d\n", i)
	}
	
	i=1: ponto inicial da variável (iniciação)
	i<=10: condição (repete enquanto i for menor ou igual a 10)
	i+=2: como a variável vai mudar em cada ciclo (incremento de 2 em 2)

EXEMPLO 3:

	for(i=10;i>0;i--){
	printf("i: %d\n", i)
	}
	
	i=10: ponto inicial da variável (iniciação)
	i>0: condição (repete enquanto i for maior que 0)
	i--: como a variável vai mudar em cada ciclo (decrimento de 1 em 1)

*/
